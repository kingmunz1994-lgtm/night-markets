// ══════════════════════════════════════════════════════════════
// buildProviders — all six required by Testnet to Preview migration
// ══════════════════════════════════════════════════════════════
async function buildProviders(): Promise<NightMarketsProviders> {

  // 1. zkConfigProvider — loads proving/verifying keys from compiled output
  const zkConfigProvider = new NodeZkConfigProvider(ZK_CONFIG_PATH);

  // 2. proofProvider — confirmed: httpClientProofProvider(proofServerUri, zkConfigProvider)
  const proofProvider = httpClientProofProvider(PROOF_SERVER, zkConfigProvider);

  // 3. publicDataProvider — confirmed: indexerPublicDataProvider(indexerUri, indexerWsUri)
  const INDEXER_WS_URI = INDEXER_URI.replace('https://', 'wss://');
  const publicDataProvider = indexerPublicDataProvider(INDEXER_URI, INDEXER_WS_URI);

  // 4. walletProvider — needed before privateStateProvider (passed in as arg)
  const walletProvider = await openWalletProvider();

  // 5. privateStateProvider — 3.0.0: lowercase function, takes walletProvider
  //    NOT LevelPrivateStateProvider.open() — that was the 2.x class-based API
  const privateStateProvider = levelPrivateStateProvider({
    privateStateStoreName: 'night-markets-state',
    walletProvider,        // walletProvider passed in — confirmed 3.0.0 pattern
  });

  // 6. midnightProvider — Midnight node RPC connection
  const midnightProvider: MidnightProvider = {
    networkId: getLedgerNetworkId(NETWORK_ID),
    nodeUri:   NODE_URI,
  };

  return {
    walletProvider,
    midnightProvider,
    publicDataProvider,
    privateStateProvider,
    proofProvider,
    zkConfigProvider,
  };
}

// ── Wallet from seed phrase ────────────────────────────────────
// For local deployment. Set WALLET_SEED in a .env file (never commit).
// For Lace-connected checkout flow in the browser, walletProvider is
// provided by the DApp Connector — see connectLace() in night-markets-v12.html.
async function openWalletProvider(): Promise<WalletProvider> {
  const seed = process.env.WALLET_SEED;
  if (!seed) {
    throw new Error(
      'WALLET_SEED not set.\n\n'
      + 'Create a .env file:\n'
      + '  WALLET_SEED=word1 word2 word3 ... word24\n\n'
      + 'This wallet must have tNight and DUST on Preprod.\n'
      + 'Faucet: https://midnight.network/faucet'
    );
  }

  const walletCtx = await Wallet.open({
    seed,
    networkId: NETWORK_ID,
    nodeUri:   NODE_URI,
  });

  // DUST guard — confirmed pattern from deployment docs:
  // wallet.state() observable, filter isSynced + dust.walletBalance > 0n
  console.log('  Waiting for wallet sync + non-zero DUST balance...');
  await new Promise<void>((resolve, reject) => {
    const sub = walletCtx.wallet.state().pipe(
      Rx.filter((s: any) => s.isSynced),
      Rx.filter((s: any) => s.dust.walletBalance(new Date()) > 0n),
      Rx.take(1),
    ).subscribe({
      next:  ()    => { sub.unsubscribe(); resolve(); },
      error: (err) => { sub.unsubscribe(); reject(err); },
    });

    // Timeout after 60s with a helpful error
    setTimeout(() => {
      sub.unsubscribe();
      reject(new Error(
        'Timed out waiting for DUST balance.\n'
        + 'In Lace: register your NIGHT UTXOs for DUST generation\n'
        + 'and wait until DUST balance is non-zero before deploying.'
      ));
    }, 60_000);
  });
  console.log('  ✓ Wallet synced with non-zero DUST balance');

  return walletCtx.wallet.asProvider();
}

