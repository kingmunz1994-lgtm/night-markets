/** createListing: seller posts listing with private IPFS metadata */
export async function callCreateListing(
  providers:     NightMarketsProviders,
  deployed:      Awaited<ReturnType<typeof deployContract>>,
  listingCid:    string,       // Opaque<"string"> IPFS CID
  sellerPkBytes: Uint8Array,   // disclose(sellerPk(localSecretKey()))
): Promise<void> {
  await submitCallTx(providers, {
    contract: deployed,
    circuit:  'createListing',
    args:     [listingCid, sellerPkBytes],
  });
}

/** fundEscrow: buyer funds the purchase */
export async function callFundEscrow(
  providers:    NightMarketsProviders,
  deployed:     Awaited<ReturnType<typeof deployContract>>,
  buyerPkBytes: Uint8Array,   // disclose(buyerPk(localSecretKey()))
  amountMicro:  bigint,       // NIGHT amount × 1_000_000n
): Promise<void> {
  await submitCallTx(providers, {
    contract: deployed,
    circuit:  'fundEscrow',
    args:     [buyerPkBytes, amountMicro],
  });
}

/** releaseEscrow: buyer confirms goods received — triggers round.increment(1) */
export async function callReleaseEscrow(
  providers: NightMarketsProviders,
  deployed:  Awaited<ReturnType<typeof deployContract>>,
): Promise<void> {
  await submitCallTx(providers, {
    contract: deployed,
    circuit:  'releaseEscrow',
    args:     [],
  });
}

/** disputeEscrow: seller raises a dispute */
export async function callDisputeEscrow(
  providers: NightMarketsProviders,
  deployed:  Awaited<ReturnType<typeof deployContract>>,
): Promise<void> {
  await submitCallTx(providers, {
    contract: deployed,
    circuit:  'disputeEscrow',
    args:     [],
  });
}

